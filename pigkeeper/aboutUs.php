    <h3>What We Do</h3>
    <p>Pigkeeper is a comprehensive platform that centralizes all aspects of pig farming operations, allowing farmers
        to:</p>
    <ul>
        <li>Schedule and manage feeding routines efficiently.</li>
        <li>Monitor the health and comfort of pigs, including advanced temperature management features.</li>
        <li>Keep accurate inventory records and financial data for better decision-making.</li>
        <li>Use real-time data analytics to determine profitability and make strategic adjustments.</li>
    </ul>
    <p>
        For farmers in remote or under-resourced areas, Pigkeeper offers an optional solar-powered LCD system, ensuring
        accessibility even in areas with limited infrastructure. This feature reflects our commitment to inclusivity and
        sustainability in agriculture.
    </p>
