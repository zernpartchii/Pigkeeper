        <h6>Proper Burial and Euthanization of Pigs Infected with ASF:</h6>
        <div>
            <p>1. Euthanization of Infected Pigs: The first step in handling ASF-infected pigs is to euthanize them to
                prevent further spread of the disease. This can be done using:</p>
            <ul>
                <li>Injection of a euthanasia solution (if available and authorized).</li>
                <li>Using a firearm or blunt trauma (if euthanasia solution is not available).</li>
            </ul>
        </div>
        <div>
            <p>2. Proper Burial Procedure: After euthanization, the infected pigs must be buried properly to prevent
                disease spread. The burial procedures include:</p>
            <ul>
                <li>Depth of the Burial: The burial pit should be at least 10 feet (approximately 3 meters) deep to
                    ensure that scavengers cannot access the carcasses.</li>
                <li>Location of the Burial: The burial site should be far from water sources, other farms, or public
                    areas to avoid contamination.</li>
                <li>Covering of Carcasses: After placing the pigs in the pit, cover them with a thick layer of soil
                    mixed with lime or disinfectants to aid decomposition and prevent disease transmission.</li>
            </ul>
        </div>
